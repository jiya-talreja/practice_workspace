def build_premarket_row():
    row = {}
    row['timestamp'] = datetime.datetime.now().strftime("%Y-%m-%d %H:%M:%S")
    row['sgx'] = fetch_sgx_nifty_change()
    row['usfut'] = fetch_us_futures_change()
    row['asiafut'] = fetch_asia_futures_change()
    row['vix'] = fetch_india_vix()
    row['bnfut'] = fetch_banknifty_fut_change()
    row['option_chain'] = fetch_option_chain_banknifty()
    ce_sign, pe_sign, heat = compute_oi_signals(row['option_chain'])
    row['ce_sign'] = ce_sign
    row['pe_sign'] = pe_sign
    row['heat'] = heat
    top5_avg, top5_vals = fetch_top5_banks_preopen()
    row['top5_avg'] = top5_avg
    row['top5_vals'] = json.dumps(top5_vals)
    dxy, crude, us10, india10 = fetch_dxy_crude_us_yields()
    row['dxy'] = dxy
    row['crude'] = crude
    row['us10'] = us10
    row['india10'] = india10
    # placeholders for data points that require custom parsing or paid APIs
    row['fii_cash'] = None
    row['fii_ratio'] = None
    row['pcr'] = None
    row['maxpain'] = None
    row['spot'] = None
    row['vol_spikes'] = None
    row['gap'] = "NO_GAP"
    row['block_signal'] = None
    row['news_flag'] = None

    score = compute_overall_signal(row)
    row['score'] = score
    if score >= 6:
        row['final'] = "Bullish"
    elif score <= -6:
        row['final'] = "Bearish"
    else:
        row['final'] = "Neutral"

    return row

