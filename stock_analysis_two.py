# ---------- DECISION / SCORING ----------
def score_signal(val, bull_thresh, bear_thresh):
    """Helper: returns +1 for bull, -1 for bear, 0 for neutral"""
    if val is None:
        return 0
    try:
        v = float(val)
    except:
        return 0
    if v >= bull_thresh:
        return 1
    if v <= bear_thresh:
        return -1
    return 0

def compute_overall_signal(rowdict):
    # rowdict contains keys from fetchers
    score = 0
    # 1. SGX (if +ve strong)
    score += score_signal(rowdict.get("sgx"), 0.25, -0.25)
    # 2. US futures
    score += score_signal(rowdict.get("usfut"), 0.25, -0.25)
    # 3. Asia futures
    score += score_signal(rowdict.get("asiafut"), 0.25, -0.25)
    # 4. VIX (lower is bull)
    vix = rowdict.get("vix")
    if vix:
        if vix < 12: score += 1
        elif vix > 16: score -= 1
    # 5. BankNifty futures % change (if available)
    score += score_signal(rowdict.get("bnfut"), 0.3, -0.3)
    # 6. FII Cash
    if rowdict.get("fii_cash") is not None:
        score += (1 if rowdict["fii_cash"] > 0 else -1)
    # 7. FII futures ratio
    if rowdict.get("fii_ratio") is not None:
        score += (1 if rowdict["fii_ratio"] > 52 else -1 if rowdict["fii_ratio"] < 48 else 0)
    # 8. PCR (lower bullish)
    pcr = rowdict.get("pcr")
    if pcr:
        score += (1 if pcr < 0.9 else -1 if pcr > 1.1 else 0)
    # 9. Max pain: if spot > maxpain => bull else bear (if maxpain exists)
    maxpain = rowdict.get("maxpain")
    spot = rowdict.get("spot")
    if maxpain is not None and spot is not None:
        score += (1 if spot > maxpain else -1)
    # 10. OI CE/PE signs
    ce_sign = rowdict.get("ce_sign")
    pe_sign = rowdict.get("pe_sign")
    if pe_sign == "PE↑": score += 1
    if ce_sign == "CE↑": score -= 1
    # 11. Heatmap
    heat = rowdict.get("heat")
    if heat == "BULL": score += 1
    if heat == "BEAR": score -= 1
    # 12. Top5Banks avg
    top5 = rowdict.get("top5_avg")
    if top5 is not None:
        score += score_signal(top5, 0.25, -0.25) * 2  # give higher weight
    # 13. DXY
    score += score_signal(rowdict.get("dxy"), -0.1, 0.1) * -1  # DXY up = bad
    # 14. Crude
    score += score_signal(rowdict.get("crude"), -0.2, 0.2) * -1
    # 15-16 yields - already included via dxy/us10 etc (skip)
    # 17. Volume spikes (count)
    vs = rowdict.get("vol_spikes")
    if vs and vs >= 2:
        score += 1 if rowdict.get("top5_avg",0) > 0 else -1
    # 18. Gap
    if rowdict.get("gap") == "GAP_UP":
        score += 1
    elif rowdict.get("gap") == "GAP_DOWN":
        score -= 1
    # 19. Block deals
    if rowdict.get("block_signal") == "BUY":
        score += 1
    elif rowdict.get("block_signal") == "SELL":
        score -= 1
    # 20. Major news flag
    if rowdict.get("news_flag") == "POS":
        score += 1
    elif rowdict.get("news_flag") == "NEG":
        score -= 1

    return score

