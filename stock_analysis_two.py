def compute_oi_signals(option_js):
    # Simplified OI sign extractor:
    # We look at calls vs puts OI change near ATM strikes in pre-market snapshot.
    if not option_js or "records" not in option_js:
        return ("MIXED","MIXED", "MIXED")
    # crude approximation: sum CE OI change and PE OI change across near strikes
    ce_change = 0
    pe_change = 0
    records = option_js["records"]
    if "data" in records:
        for row in records["data"]:
            ce = row.get("CE")
            pe = row.get("PE")
            if ce and "changeinOpenInterest" in ce:
                ce_change += float(ce["changeinOpenInterest"] or 0)
            if pe and "changeinOpenInterest" in pe:
                pe_change += float(pe["changeinOpenInterest"] or 0)
    ce_sign = ("CE↑" if ce_change>0 else "CE↓" if ce_change<0 else "MIXED")
    pe_sign = ("PE↑" if pe_change>0 else "PE↓" if pe_change<0 else "MIXED")
    # heatmap signal: compare aggregate
    heat = "BULL" if pe_change>ce_change else "BEAR" if ce_change>pe_change else "MIXED"
    return (ce_sign, pe_sign, heat)

def fetch_top5_banks_preopen():
    # Use Yahoo Finance / TradingView data for pre-market quotes
    banks = {
        "HDFCBANK.NS":"HDFCBANK",
        "ICICIBANK.NS":"ICICIBANK",
        "SBIN.NS":"SBIN",
        "AXISBANK.NS":"AXISBANK",
        "KOTAKBANK.NS":"KOTAKBANK"
    }
    vals = {}
    for sym in banks:
        try:
            url = f"https://query1.finance.yahoo.com/v7/finance/quote?symbols={sym}"
            r = requests.get(url, timeout=6, headers=HEADERS)
            js = r.json()
            if "quoteResponse" in js and js["quoteResponse"]["result"]:
                q = js["quoteResponse"]["result"][0]
                prev = q.get("regularMarketPreviousClose")
                price = q.get("regularMarketPrice")
                if price and prev:
                    vals[banks[sym]] = ((price - prev) / prev) * 100
        except:
            vals[banks[sym]] = None
    # compute average of available
    nums = [v for v in vals.values() if v is not None]
    avg = (sum(nums)/len(nums)) if nums else None
    return avg, vals

